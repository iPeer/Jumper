package ipeer.jumper.error;

public class IncorrectLevelSizeError extends Exception {

	private static final long serialVersionUID = 7672418542027362859L;

	public IncorrectLevelSizeError() {
	}

	public IncorrectLevelSizeError(String arg0) {
		super(arg0);
	}

	public IncorrectLevelSizeError(Throwable arg0) {
		super(arg0);
	}

	public IncorrectLevelSizeError(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
